    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String id = jTextField7.getText();
        try {
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from staff where staff_id='" + id + "' ");
            if (rs.first()) {
                jTextField1.setText(rs.getString(2));
                jTextField5.setText(rs.getString(3));
                jTextField6.setText(rs.getString(4));
                jDateChooser1.setDate(rs.getDate(5));
                jTextField2.setText(rs.getString(6));
                jTextField3.setText(rs.getString(7));
                jComboBox3.setSelectedItem(rs.getString(8));
                jComboBox4.setSelectedItem(rs.getString(10));
                jTextField4.setText(rs.getString(11));
                byte[] imageData = rs.getBytes("picture");
                ImageIcon icon = new ImageIcon(imageData);
                Image img = icon.getImage().getScaledInstance(lbl_picture.getWidth(), lbl_picture.getHeight(), Image.SCALE_SMOOTH);
                lbl_picture.setIcon(new ImageIcon(img));
            } else {
                JFrame jf = new JFrame();
                jf.setAlwaysOnTop(true);
                JOptionPane.showMessageDialog(jf, "Staff id does not exist");
            }
        } catch (Exception e) {
            JFrame jf = new JFrame();
            jf.setAlwaysOnTop(true);
            JOptionPane.showMessageDialog(jf, e);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

