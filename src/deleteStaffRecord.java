    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int staffId = Integer.parseInt(jTextField7.getText());

        int result = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete staff record?", "Confirmation", JOptionPane.YES_NO_OPTION);

        if (result == JOptionPane.YES_OPTION) {
            try {
                Connection con = ConnectionProvider.getCon();
                PreparedStatement ps = con.prepareStatement("delete from staff where staff_id=?");
                ps.setInt(1, staffId);
                ps.execute();
                JFrame jf = new JFrame();
                jf.setAlwaysOnTop(true);
                JOptionPane.showMessageDialog(jf, "Successfully deleted");
                setVisible(false);
                new deleteStaffRecord().setVisible(true);
            } catch (Exception e) {
                JFrame jf = new JFrame();
                jf.setAlwaysOnTop(true);
                JOptionPane.showMessageDialog(jf, e);
            }
        }    
    }//GEN-LAST:event_jButton3ActionPerformed

